# In Python, a variable is a named reference to a value stored in memory. Variables are dynamically typed, meaning you don't need to declare their type; Python determines it based on the value assigned.

a=34
print(type(a),a)
b=23.4567
print(type(b),b)
c=True
print(type(c),c)
nums={2,34,567}
fav=["apple","mango"]
car=("Honda",234)
print(type(nums),nums)
print(type(fav),fav)
print(type(car),car)